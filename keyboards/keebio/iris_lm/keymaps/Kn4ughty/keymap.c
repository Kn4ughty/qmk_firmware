typedef struct key_rgb {
    uint8_t index;
    uint8_t r;
    uint8_t g;
    uint8_t b;
} KeyRGB;

KeyRGB bytes_to_key(uint8_t bytes[4]) {
    struct key_rgb key = {
        bytes[0],
        bytes[1],
        bytes[2],
        bytes[3],
    };
    return key;
}

